#include <bits/stdc++.h>

using namespace std;

int n,m;
bool tab[2001][2001];
char temp;
int main()
{

    scanf("%i",&n);
    scanf("%i",&m);
    for(int i=0;i<n;i++){
        for(int j=0;j<m;j++){
            scanf("%c",&temp);
            if(temp=='.') tab[i][j]=0;
            else tab[i][j]=1;
        }
    }

    return 0;
}
