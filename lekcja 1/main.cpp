#include <bits/stdc++.h>

#define lld long long
using namespace std;
lld n;
lld m;

lld nwd(lld n, lld m){
    while(n!=m){
        if(n>m) n=n-m;
        if(n<m) m=m-n;

    }
    return m;
}
int main()
{
    scanf("%lld",&n);
    scanf("%lld",&m);
    printf("%lld",nwd(n,m));
    return 0;
}
